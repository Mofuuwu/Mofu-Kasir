    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
        String query = "INSERT INTO barang (nama_barang, harga, stok, tanggal_masuk, tanggal_kadaluarsa) VALUES (?, ?, ?, ?, ?)";
        PreparedStatement pstmt = mysqlconfig.prepareStatement(query);
        
        pstmt.setString(1, inputNamaBarang.getText());
        pstmt.setString(2, inputHargaBarang.getText());
        pstmt.setString(3, inputStokBarang.getText());
        pstmt.setString(4, inputTanggalMasuk.getText());
        pstmt.setString(5, inputKadaluarsa.getText());
        
        int rowsInserted = pstmt.executeUpdate();
        
        if (rowsInserted > 0) {
            JOptionPane.showMessageDialog(null, "Data Berhasil Ditambahkan");
            kosong();
            loadData();
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        int selectedRow = tabelBarang.getSelectedRow();
        if (selectedRow != -1) {
            String namaBarangBaru = inputNamaBarang.getText();
            String hargaBarangBaru = inputHargaBarang.getText();
            String stokBarangBaru = inputStokBarang.getText();
            String tanggalMasukBaru = inputTanggalMasuk.getText();
            String tanggalKadaluarsaBaru = inputKadaluarsa.getText();

            String kodeBarang = tabelBarang.getValueAt(selectedRow, tabelBarang.getColumn("KODE")
                    .getModelIndex()).toString();

            try {
                
                String sql = "UPDATE barang SET nama_barang = ?, harga = ?, stok = ?, tanggal_masuk = ?, "
                        + "tanggal_kadaluarsa = ? WHERE kode_barang = ?";
                PreparedStatement pstmt = mysqlconfig.prepareStatement(sql);
                pstmt.setString(1, namaBarangBaru);
                pstmt.setString(2, hargaBarangBaru);
                pstmt.setString(3, stokBarangBaru);
                pstmt.setString(4, tanggalMasukBaru);
                pstmt.setString(5, tanggalKadaluarsaBaru);
                pstmt.setString(6, kodeBarang);

                int updatedRows = pstmt.executeUpdate();
                if (updatedRows > 0) {
                    JOptionPane.showMessageDialog(this, "Data berhasil diupdate.", 
                            "Informasi", JOptionPane.INFORMATION_MESSAGE);
                    loadData();
                } else {
                    JOptionPane.showMessageDialog(this, "Gagal mengupdate data.", 
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
                pstmt.close();
                
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), 
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris tabel terlebih dahulu.", 
                    "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btnEditActionPerformed

